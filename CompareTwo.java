public class CompareTwo {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 20;
		int num3 = 10;
		int num4 = 30;
		
		System.out.println(Integer.compare(num1, num2));
		
		System.out.println(Integer.compare(num1, num3));
		
		System.out.println(Integer.compare(num4, num2));
		
	}

}
